package com.cn.uk.model;

public class DoorAccessCard {

    private String empNo;
    private String cardFixNo;
    private String cardStatusid;
    private String empCardId;

    public String getEmpNo() {
        return empNo;
    }

    public void setEmpNo(String empNo) {
        this.empNo = empNo;
    }

    public String getCardFixNo() {
        return cardFixNo;
    }

    public void setCardFixNo(String cardFixNo) {
        this.cardFixNo = cardFixNo;
    }

    public String getCardStatusid() {
        return cardStatusid;
    }

    public void setCardStatusid(String cardStatusid) {
        this.cardStatusid = cardStatusid;
    }

    public String getEmpCardId() {
        return empCardId;
    }

    public void setEmpCardId(String empCardId) {
        this.empCardId = empCardId;
    }
}
